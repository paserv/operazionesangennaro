package it.osg.model;

public class Color {

	public String R;
	public String G;
	public String B;
	
	public Color(String erre, String gi, String bi) {
		this.R = erre;
		this.G = gi;
		this.B = bi;
	}
	
	public String getR() {
		return R;
	}
	public void setR(String r) {
		R = r;
	}
	public String getG() {
		return G;
	}
	public void setG(String g) {
		G = g;
	}
	public String getB() {
		return B;
	}
	public void setB(String b) {
		B = b;
	}
	
	
	
}
